import Foundation
import UIKit

extension UIColor {
    @nonobjc class var softPink: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 211.0 / 255.0, blue: 220.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var softSky: UIColor {
        return UIColor(red: 184.0 / 255.0, green: 238.0 / 255.0, blue: 255.0 / 255.0, alpha: 1.0)
    }
}
